package animation_chenile;

public class salade {
	
	    private int x;
	    private int y;
	    private int r;

	    public salade(int x, int y, int r) {
	        this.x = x;
	        this.y = y;
	        this.r = r;
	    }

	    public int getX() {
	        return x;
	    }

	    public int getY() {
	        return y;
	    }

	    public int getR() {
	        return r;
	    }
}